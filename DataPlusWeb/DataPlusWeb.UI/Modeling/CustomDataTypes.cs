
namespace DataPlus.Web.UI
{
    public static class CustomDataTypes
    {
        #region Public fields region

        /// <summary>
        /// Represents a date value.
        /// </summary>
        public const string Date = "date";

        /// <summary>
        /// Represents a time value.
        /// </summary>
        public const string Time = "time";

        /// <summary>
        /// Represents an instant in time, expressed as a date and time of day.
        /// </summary>
        public const string DateTime = "datetime";

        /// <summary>
        /// Represents multi-line text.
        /// </summary>
        public const string MultilineText = "multilinetext";

        /// <summary>
        /// Represents a continuous time during which an object exists.
        /// </summary>
        public const string Duration = "duration";

        /// <summary>
        /// Represents a continuous time during which an object exists and start date time.
        /// </summary>
        public const string Period = "period";

        #endregion
    }
}
